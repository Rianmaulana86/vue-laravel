<script>
import axios from 'axios';

    export default {

        data() {
            return {
                form: {
                    title: "",

                    description: "",

                    subject: "",
                },

                    loading: false,
                    
                    subjects: [],

                    theErrors: [],

            };
        },


        mounted() {
            this.getSubjects();
        },

        methods: {
            async getSubjects() {
                let response = await axios.get('/api/subject')
                if (response.status === 200) {
                    this.subjects = response.data
                }
            },

            async store() {

                this.loading = true;

            try {
                let response = await axios.post('/api/create-note', this.form)

                if (response.status == 200) {
                    this.form.title = ""

                    this.form.description = ""

                    this.form.subject = ""

                    this.theErrors= []

                    this.loading= false;

                    this.$toasted.show(response.data.message, {
                        type: 'success',

                        duration: 4000,
                    });
                }

                }  catch (e) {

                    this.loading= false;
                     this.$toasted.show("Something when wrong", {
                        type: 'error',

                        duration: 4000,
                    });
                
                
                this.theErrors = e.response.data.errors;
            }
            } 
        }
    }


</script>


